// What is a collection in MongoDB?
// Collections in MongoDB are containers for storing documents.
// Each collection is analogous to a table in a relational database and can hold multiple documents.
// Collections do not enforce a schema, allowing documents within the same collection to have different structures.
// Collections are created implicitly when documents are inserted into them.


