// What is the difference between MongoDB
// and a relational database?
// MongoDB is a NoSQL database, while relational databases are SQL-based.
// MongoDB stores data in flexible, JSON-like documents, while relational databases store data in tables with rows and columns.
// MongoDB does not require predefined schemas, while relational databases enforce a schema for data consistency.
// MongoDB is designed for horizontal scaling and can handle large datasets, while relational databases are typically scaled vertically.
// MongoDB uses its own query language based on JSON, while relational databases use SQL for querying data.
// MongoDB is a good fit for applications that require flexibility and scalability, while relational databases are better suited for applications with complex relationships and transactions.


